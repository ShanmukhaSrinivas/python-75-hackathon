#indexing
x='welcome to hackathon'

#prints 3rd character from begining
print(x[2])

#prints 7th character from begining
print(x[6])

#prints 3rd character from end
print(x[15])

#prints the last character
print(x[len(x)-1])

#above line can be written as
print(x[19])

#whitespaces are also taken 
print(x[7])
